-- 퀴즈 히스토리 테이블 삭제(PostgreSQL)
DROP TABLE IF EXISTS QUIZ_HISTORY;
-- 퀴즈 테이블 삭제(PostgreSQL)
DROP TABLE IF EXISTS QUIZ_MASTER;
-- 카테고리 테이블 삭제(PostgreSQL)
DROP TABLE IF EXISTS CATEGORY;

-- 카테고리 테이블 생성(PostgreSQL)
CREATE TABLE CATEGORY (
    CATEGORY_CD VARCHAR(50) PRIMARY KEY,
    CATEGORY_NM VARCHAR(100) NOT NULL,
    USE_YN CHAR(1) DEFAULT 'Y',
    REG_DTM TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    MODI_DTM TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE CATEGORY IS '카테고리';
COMMENT ON COLUMN CATEGORY.CATEGORY_CD IS '카테고리 코드(PK)';
COMMENT ON COLUMN CATEGORY.CATEGORY_NM IS '카테고리명';
COMMENT ON COLUMN CATEGORY.USE_YN IS '사용여부';
COMMENT ON COLUMN CATEGORY.REG_DTM IS '등록 일시';
COMMENT ON COLUMN CATEGORY.MODI_DTM IS '수정 일시';

-- 퀴즈 테이블 생성(PostgreSQL)
CREATE TABLE QUIZ_MASTER (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CATEGORY_CD VARCHAR(50) NOT NULL,
    QUESTION TEXT NOT NULL,
    OPTION_1 VARCHAR(500) NOT NULL,
    OPTION_2 VARCHAR(500) NOT NULL,
    OPTION_3 VARCHAR(500) NOT NULL,
    OPTION_4 VARCHAR(500) NOT NULL,
    ANSWER INT NOT NULL,
    EXPLANATION TEXT,
    LEVEL INT DEFAULT 1,
    RANDOM_YN CHAR(1) DEFAULT 'N',
    USE_YN CHAR(1) DEFAULT 'Y',
    REG_DTM TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    MODI_DTM TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT FK_QUIZ_MASTER_CATEGORY_CD FOREIGN KEY (CATEGORY_CD) REFERENCES CATEGORY (CATEGORY_CD) ON DELETE CASCADE
);
COMMENT ON TABLE QUIZ_MASTER IS '퀴즈 마스터';
COMMENT ON COLUMN QUIZ_MASTER.ID IS '퀴즈 아이디(PK)';
COMMENT ON COLUMN QUIZ_MASTER.CATEGORY_CD IS '카테고리 코드(FK)';
COMMENT ON COLUMN QUIZ_MASTER.QUESTION IS '문제 내용';
COMMENT ON COLUMN QUIZ_MASTER.OPTION_1 IS '보기 1';
COMMENT ON COLUMN QUIZ_MASTER.OPTION_2 IS '보기 2';
COMMENT ON COLUMN QUIZ_MASTER.OPTION_3 IS '보기 3';
COMMENT ON COLUMN QUIZ_MASTER.OPTION_4 IS '보기 4';
COMMENT ON COLUMN QUIZ_MASTER.ANSWER IS '정답 번호';
COMMENT ON COLUMN QUIZ_MASTER.EXPLANATION IS '해설';
COMMENT ON COLUMN QUIZ_MASTER.LEVEL IS '난이도';
COMMENT ON COLUMN QUIZ_MASTER.RANDOM_YN IS '랜덤 보기 표시 여부';
COMMENT ON COLUMN QUIZ_MASTER.USE_YN IS '사용여부';
COMMENT ON COLUMN QUIZ_MASTER.REG_DTM IS '등록 일시';
COMMENT ON COLUMN QUIZ_MASTER.MODI_DTM IS '수정 일시';

-- 퀴즈 히스토리 테이블 생성(PostgreSQL)
CREATE TABLE QUIZ_HISTORY (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    QUIZ_ID BIGINT NOT NULL,
    CHOICE_OPTION INT NOT NULL,
    CORRECT_YN CHAR(1) NOT NULL,
    REG_DTM TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT FK_QUIZ_HISTORY_QUIZ_ID FOREIGN KEY (QUIZ_ID) REFERENCES QUIZ_MASTER (ID) ON DELETE CASCADE
);
COMMENT ON TABLE QUIZ_HISTORY IS '퀴즈 풀이 히스토리';
COMMENT ON COLUMN QUIZ_HISTORY.ID IS '히스토리 아이디(PK)';
COMMENT ON COLUMN QUIZ_HISTORY.QUIZ_ID IS '퀴즈 아이디(FK)';
COMMENT ON COLUMN QUIZ_HISTORY.CHOICE_OPTION IS '사용자 선택 번호';
COMMENT ON COLUMN QUIZ_HISTORY.CORRECT_YN IS '정답 여부';
COMMENT ON COLUMN QUIZ_HISTORY.REG_DTM IS '풀이 일시';

-- 통계 조회를 위한 인덱스
CREATE INDEX IDX_QUIZ_HISTORY_QUIZ_ID ON QUIZ_HISTORY(QUIZ_ID);
CREATE INDEX IDX_QUIZ_HISTORY_REG_DTM ON QUIZ_HISTORY(REG_DTM);